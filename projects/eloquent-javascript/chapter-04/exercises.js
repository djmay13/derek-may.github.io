////////////////////////////////////////////////////////////////////////////////
// deepEqual ///////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function deepEqual(x, y) {
    //determine if parameters are simple data types
      if(typeof x !== 'object' && typeof y !== 'object'){
    //if true, compare params
        return x === y;
      }
    //determine if only one param is a simple data type 
      if(typeof x !== 'object' || typeof y !== 'object'){
    //if true, return false
        return false;
      }
    //create an array of keys of params
      let xKeys = Object.keys(x);
      let yKeys = Object.keys(y);
    //iterate through param keys
      for(let i = 0; i < xKeys.length; i++){
    //determine if y param keys does not include current x param key or param keys are not same
        if(!yKeys.includes(xKeys[i]) || !deepEqual(x[xKeys[i]], y[xKeys[i]])){
    //if true, return false
        return false;
        }
      }
      return true;
}

